"""Fit every model on the train window and score it on the temporal holdout.

Writes eval/results.md with the leaderboard + a cold-start breakdown. Every number the
README quotes is copied from this file -- nothing is hand-entered.
"""
from __future__ import annotations

import os

os.environ.setdefault("OPENBLAS_NUM_THREADS", "1")  # implicit ALS perf + quiet threadpool

import pathlib
import sys
import time

import numpy as np

from data import build_dataset
from metrics import evaluate, ndcg_at_k
from models import ALSRecommender, ItemKNNRecommender, PopularityRecommender

K = 10
OUT = pathlib.Path(__file__).parent / "eval"


def cold_start_breakdown(models: dict, ds, k: int = K) -> tuple[int, dict]:
    """NDCG@k split by how much TRAIN history each test user has (median split).
    Popularity often wins for thin-history users -- a point worth showing, not hiding."""
    hist = {u: len(ds.seen_items(u)) for u in ds.test}
    median = int(np.median(list(hist.values())))
    rows = {}
    for name, model in models.items():
        low, high = [], []
        for u, relevant in ds.test.items():
            rec = model.recommend(u, k=k, exclude_seen=True)
            score = ndcg_at_k(rec, relevant, k)
            (low if hist[u] <= median else high).append(score)
        rows[name] = (float(np.mean(low)), float(np.mean(high)), len(low), len(high))
    return median, rows


def main() -> None:
    if hasattr(sys.stdout, "reconfigure"):
        sys.stdout.reconfigure(encoding="utf-8")

    ds = build_dataset()
    models = {
        "Popularity": PopularityRecommender().fit(ds.train),
        "ItemKNN": ItemKNNRecommender(topk_neighbors=50).fit(ds.train),
        "ALS": ALSRecommender(factors=64, iterations=20, regularization=0.05).fit(ds.train),
    }

    results = {}
    for name, model in models.items():
        t0 = time.time()
        results[name] = evaluate(model, ds, k=K)
        results[name]["fit+eval_s"] = round(time.time() - t0, 2)

    median, cold = cold_start_breakdown(models, ds)

    OUT.mkdir(exist_ok=True)
    lines = []
    lines.append("# Recsys Studio — evaluation results\n")
    lines.append("MovieLens-100k, implicit feedback (rating >= 4), **global temporal split** "
                 f"(80% of interactions by time = train, last 20% = test).\n")
    lines.append(f"- Train: **{ds.n_users} users x {ds.n_items} items**, "
                 f"{ds.n_train_events:,} positive events\n")
    lines.append(f"- Test:  **{len(ds.test)} users**, {ds.n_test_events:,} held-out future "
                 f"positives (cutoff ts = {ds.cutoff_ts})\n")
    lines.append("\n## Leaderboard (@%d)\n" % K)
    lines.append("| Model | NDCG@10 | Recall@10 | MAP@10 | Coverage | Novelty (bits) |")
    lines.append("| --- | --- | --- | --- | --- | --- |")
    for name in ("Popularity", "ItemKNN", "ALS"):
        r = results[name]
        lines.append(f"| {name} | {r['NDCG@10']:.4f} | {r['Recall@10']:.4f} | "
                     f"{r['MAP@10']:.4f} | {r['Coverage']:.3f} | {r['Novelty(bits)']:.2f} |")

    lines.append(f"\n## Cold-start: NDCG@10 by train-history (median split = {median} items)\n")
    lines.append("| Model | Low-history users | High-history users |")
    lines.append("| --- | --- | --- |")
    for name in ("Popularity", "ItemKNN", "ALS"):
        lo, hi, nlo, nhi = cold[name]
        lines.append(f"| {name} | {lo:.4f} (n={nlo}) | {hi:.4f} (n={nhi}) |")

    lines.append("\n_Generated by `eval.py` — copy these numbers verbatim into the README._\n")
    (OUT / "results.md").write_text("\n".join(lines), encoding="utf-8")

    # Console summary
    print(f"Test users: {len(ds.test)} | held-out positives: {ds.n_test_events}")
    print(f"{'Model':<12}{'NDCG@10':>10}{'Recall@10':>11}{'MAP@10':>9}"
          f"{'Coverage':>10}{'Novelty':>9}")
    for name in ("Popularity", "ItemKNN", "ALS"):
        r = results[name]
        print(f"{name:<12}{r['NDCG@10']:>10.4f}{r['Recall@10']:>11.4f}{r['MAP@10']:>9.4f}"
              f"{r['Coverage']:>10.3f}{r['Novelty(bits)']:>9.2f}")
    print(f"\nCold-start (NDCG@10, median history = {median}):")
    for name in ("Popularity", "ItemKNN", "ALS"):
        lo, hi, nlo, nhi = cold[name]
        print(f"  {name:<12} low={lo:.4f} (n={nlo})  high={hi:.4f} (n={nhi})")
    print(f"\nWrote {OUT / 'results.md'}")


if __name__ == "__main__":
    main()
